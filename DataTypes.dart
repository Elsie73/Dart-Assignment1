// Dart program demonstrating various data types

void main() {
  // Integer
  int myInt = 10;
  print('Integer: $myInt');

  // Double
  double myDouble = 3.14;
  print('Double: $myDouble');

  // String
  String myString = 'Hello, i am Elsie!';
  print('String: $myString');

  // List
  List<int> myList = [1, 2, 3, 4, 5];
  print('List: $myList');

  // Map
  Map<String, int> myMap = {'apple': 5, 'banana': 10, 'orange': 7};
  print('Map: $myMap');
}
